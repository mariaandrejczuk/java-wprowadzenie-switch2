public class ColorPsychology {
    public static void main(String[] args) {
        char color = 'n';
        switch (color) {
            case 'c':
                System.out.println("pomidor");
                break;
            case 'z':
                System.out.println("trawa");
                break;
            case 'n':
                System.out.println("morze");
                break;
            default:
                System.out.println("brak skojarze≈Ñ");
        }
    }
}
